    @staticmethod
    def _clean_llm_json(text: str) -> str:
        """РћС‡РёСЃС‚РёС‚СЊ РѕС‚РІРµС‚ LLM РѕС‚ markdown-РѕР±С‘СЂС‚РѕРє Рё С‚РёРїРёС‡РЅС‹С… РѕС€РёР±РѕРє GigaChat.

        GigaChat С‡Р°СЃС‚Рѕ РѕР±РѕСЂР°С‡РёРІР°РµС‚ JSON РІ ```json ... ```, РґРѕР±Р°РІР»СЏРµС‚ trailing commas,
        РёР»Рё РІСЃС‚Р°РІР»СЏРµС‚ РїРѕСЏСЃРЅРµРЅРёСЏ РґРѕ/РїРѕСЃР»Рµ JSON.
        """
        # РЈР±РёСЂР°РµРј markdown code block РѕР±С‘СЂС‚РєРё
        text = re.sub(r'```(?:json)?\s*\n?', '', text)
        text = re.sub(r'\n?```\s*$', '', text, flags=re.MULTILINE)
        # РЈР±РёСЂР°РµРј trailing commas РїРµСЂРµРґ } РёР»Рё ]
        text = re.sub(r',\s*([}\]])', r'\1', text)
        return text.strip()

    def _parse_tool_call(
        self, response: str, retry_on_fail: bool = True, _original: str | None = None,
    ) -> dict[str, Any]:
        """РР·РІР»РµС‡СЊ РІС‹Р·РѕРІ РёРЅСЃС‚СЂСѓРјРµРЅС‚Р° РёР· РѕС‚РІРµС‚Р° LLM.

        РСЃРїРѕР»СЊР·СѓРµС‚ РїР°СЂСЃРµСЂ СЃ РїРѕРґСЃС‡С‘С‚РѕРј РіР»СѓР±РёРЅС‹ СЃРєРѕР±РѕРє Рё СѓС‡С‘С‚РѕРј СЃС‚СЂРѕРєРѕРІС‹С… Р»РёС‚РµСЂР°Р»РѕРІ,
        С‡С‚РѕР±С‹ РєРѕСЂСЂРµРєС‚РЅРѕ РѕР±СЂР°Р±Р°С‚С‹РІР°С‚СЊ РІР»РѕР¶РµРЅРЅС‹Рµ РѕР±СЉРµРєС‚С‹ (РЅР°РїСЂРёРјРµСЂ, args СЃ SQL РІРЅСѓС‚СЂРё).
        РџСЂРё РЅРµСѓРґР°С‡Рµ вЂ” retry СЃ СѓС‚РѕС‡РЅСЏСЋС‰РёРј РїСЂРѕРјРїС‚РѕРј.
        """
        original_response = _original or response
        cleaned = self._clean_llm_json(response)

        # РС‰РµРј РІСЃРµ JSON-РѕР±СЉРµРєС‚С‹ РІРµСЂС…РЅРµРіРѕ СѓСЂРѕРІРЅСЏ СЃ СѓС‡С‘С‚РѕРј РІР»РѕР¶РµРЅРЅРѕСЃС‚Рё Рё СЃС‚СЂРѕРє
        for candidate in self._extract_json_objects(cleaned):
            try:
                parsed = json.loads(candidate)
                if isinstance(parsed, dict) and "tool" in parsed:
                    return parsed
            except (json.JSONDecodeError, ValueError):
                continue

        # РўР°РєР¶Рµ РїСЂРѕР±СѓРµРј РёР· РѕСЂРёРіРёРЅР°Р»СЊРЅРѕРіРѕ РѕС‚РІРµС‚Р° (РЅР° СЃР»СѓС‡Р°Р№ РµСЃР»Рё clean СЃР»РѕРјР°Р» С‡С‚Рѕ-С‚Рѕ)
        if cleaned != response:
            for candidate in self._extract_json_objects(response):
                try:
                    parsed = json.loads(candidate)
                    if isinstance(parsed, dict) and "tool" in parsed:
                        return parsed
                except (json.JSONDecodeError, ValueError):
                    continue

        # Попытка восстановления через json_repair
        if repair_json is not None:
            try:
                repaired = repair_json(cleaned)
                parsed = json.loads(repaired)
                if isinstance(parsed, dict) and "tool" in parsed:
                    logger.info("json_repair recovered valid tool call")
                    return parsed
            except (json.JSONDecodeError, ValueError, Exception):
                pass

        # Retry: РїРѕРІС‚РѕСЂРЅС‹Р№ Р·Р°РїСЂРѕСЃ Рє LLM СЃ СѓС‚РѕС‡РЅРµРЅРёРµРј С„РѕСЂРјР°С‚Р°
        if retry_on_fail:
            logger.warning("JSON РЅРµ РЅР°Р№РґРµРЅ РІ РѕС‚РІРµС‚Рµ LLM, retry СЃ СѓС‚РѕС‡РЅРµРЅРёРµРј С„РѕСЂРјР°С‚Р°")
            retry_system = (
                "РўС‹ вЂ” С„РѕСЂРјР°С‚РёСЂРѕРІС‰РёРє РѕС‚РІРµС‚РѕРІ. РџСЂРµРѕР±СЂР°Р·СѓР№ С‚РµРєСЃС‚ РІ JSON-РѕР±СЉРµРєС‚.\n"
                "Р’РµСЂРЅРё РўРћР›Р¬РљРћ РѕРґРёРЅ JSON-РѕР±СЉРµРєС‚, Р±РµР· РїРѕСЏСЃРЅРµРЅРёР№, Р±РµР· markdown-РѕР±С‘СЂС‚РѕРє."
            )
            retry_user = (
                "РџСЂРµРґС‹РґСѓС‰РёР№ РѕС‚РІРµС‚ РЅРµ СЃРѕРґРµСЂР¶РёС‚ РІР°Р»РёРґРЅС‹Р№ JSON.\n"
                f"РћС‚РІРµС‚: {response[:1500]}\n\n"
                "Р’РµСЂРЅРё РўРћР›Р¬РљРћ РѕРґРёРЅ JSON-РѕР±СЉРµРєС‚ РІ РѕРґРЅРѕРј РёР· С„РѕСЂРјР°С‚РѕРІ:\n"
                '{"tool": "execute_query", "args": {"sql": "SELECT ..."}}\n'
                '{"tool": "get_table_columns", "args": {"schema": "dm", "table": "clients"}}\n'
                '{"tool": "none", "result": "С‚РµРєСЃС‚РѕРІС‹Р№ РѕС‚РІРµС‚"}'
            )
            retry_response = str(self.llm.invoke_with_system(
                retry_system, retry_user, temperature=0.0,
            ))
            return self._parse_tool_call(
                retry_response, retry_on_fail=False, _original=original_response,
            )

        return {"tool": "none", "result": original_response}

    @staticmethod
    def _extract_json_objects(text: str) -> list[str]:
        """РР·РІР»РµС‡СЊ JSON-РѕР±СЉРµРєС‚С‹ РёР· С‚РµРєСЃС‚Р° СЃ СѓС‡С‘С‚РѕРј РІР»РѕР¶РµРЅРЅС‹С… СЃРєРѕР±РѕРє Рё СЃС‚СЂРѕРєРѕРІС‹С… Р»РёС‚РµСЂР°Р»РѕРІ.

        Returns:
            РЎРїРёСЃРѕРє СЃС‚СЂРѕРє-РєР°РЅРґРёРґР°С‚РѕРІ JSON-РѕР±СЉРµРєС‚РѕРІ.
        """
        candidates = []
        i = 0
        while i < len(text):
            if text[i] == '{':
                depth = 0
                in_string = False
                escape_next = False
                start = i
                for j in range(i, len(text)):
                    ch = text[j]
                    if escape_next:
                        escape_next = False
                        continue
                    if ch == '\\' and in_string:
                        escape_next = True
                        continue
                    if ch == '"' and not escape_next:
                        in_string = not in_string
                        continue
                    if in_string:
                        continue
                    if ch == '{':
                        depth += 1
                    elif ch == '}':
                        depth -= 1
                        if depth == 0:
                            candidates.append(text[start:j + 1])
                            i = j
                            break
                else:
                    break
            i += 1
        return candidates

    def _check_disambiguation_needed(
        self, tool_name: str, result: str, user_input: str,
    ) -> list[dict[str, Any]] | None:
        """РџСЂРѕРІРµСЂРёС‚СЊ, РІРµСЂРЅСѓР» Р»Рё РїРѕРёСЃРє РЅРµСЃРєРѕР»СЊРєРѕ С‚Р°Р±Р»РёС†, С‚СЂРµР±СѓСЋС‰РёС… СѓС‚РѕС‡РЅРµРЅРёСЏ.

        Returns:
            РЎРїРёСЃРѕРє РѕРїС†РёР№ РґР»СЏ РІС‹Р±РѕСЂР° РїРѕР»СЊР·РѕРІР°С‚РµР»РµРј РёР»Рё None.
        """
        if tool_name not in ("search_tables", "search_by_description"):
            return None

        # Р’РѕРїСЂРѕСЃС‹-РїРµСЂРµС‡РёСЃР»РµРЅРёСЏ ("РєР°РєРёРµ РІРёС‚СЂРёРЅС‹ РµСЃС‚СЊ?") РЅРµ С‚СЂРµР±СѓСЋС‚ disambiguation вЂ”
        # РїРѕР»СЊР·РѕРІР°С‚РµР»СЊ С…РѕС‡РµС‚ СѓРІРёРґРµС‚СЊ Р’РЎР• СЂРµР·СѓР»СЊС‚Р°С‚С‹, Р° РЅРµ РІС‹Р±СЂР°С‚СЊ РѕРґРёРЅ
        listing_patterns = [
            r'РєР°РєРёРµ\s+(РІРёС‚СЂРёРЅС‹|С‚Р°Р±Р»РёС†С‹|РґР°РЅРЅС‹Рµ)',
            r'РїРѕРєР°Р¶Рё\s+(РІСЃРµ|РІРёС‚СЂРёРЅС‹|С‚Р°Р±Р»РёС†С‹)',
            r'С‡С‚Рѕ\s+(С‚С‹\s+)?Р·РЅР°РµС€СЊ',
            r'СЃРїРёСЃРѕРє\s+(РІРёС‚СЂРёРЅ|С‚Р°Р±Р»РёС†)',
            r'РїРµСЂРµС‡РёСЃР»Рё',
            r'РєР°РєРёРµ\s+РµСЃС‚СЊ',
        ]
        user_lower = user_input.lower()
        if any(re.search(p, user_lower) for p in listing_patterns):
            return None

        match = re.search(r'РќР°Р№РґРµРЅРѕ С‚Р°Р±Р»РёС†:\s*(\d+)', result)
        if not match or int(match.group(1)) <= 1:
            return None

        # РР·РІР»РµРєР°РµРј schema.table РёР· СЃС‚СЂРѕРє СЂРµР·СѓР»СЊС‚Р°С‚Р°
        table_pattern = re.compile(r'^\s+(\w+)\.(\w+)\s*вЂ”\s*(.*)$', re.MULTILINE)
        options: list[dict[str, Any]] = []
        for m in table_pattern.finditer(result):
            schema_name, table_name, description = (
                m.group(1), m.group(2), m.group(3).strip(),
            )
            options.append({
                "schema": schema_name,
                "table": table_name,
                "description": description,
                "key_columns": self.schema.get_primary_keys(schema_name, table_name),
            })

        if len(options) <= 1:
            return None

        # Р•СЃР»Рё РїРѕР»СЊР·РѕРІР°С‚РµР»СЊ СѓР¶Рµ СѓРєР°Р·Р°Р» РєРѕРЅРєСЂРµС‚РЅСѓСЋ С‚Р°Р±Р»РёС†Сѓ вЂ” РЅРµ СЃРїСЂР°С€РёРІР°РµРј РїРѕРІС‚РѕСЂРЅРѕ
        user_lower = user_input.lower()
        for opt in options:
            full_name = f"{opt['schema']}.{opt['table']}"
            if full_name.lower() in user_lower:
                return None

        return options

    def _call_tool(self, tool_name: str, args: dict[str, Any]) -> ToolResult:
        """Р’С‹Р·РІР°С‚СЊ РёРЅСЃС‚СЂСѓРјРµРЅС‚ РїРѕ РёРјРµРЅРё СЃ РІР°Р»РёРґР°С†РёРµР№ Р°СЂРіСѓРјРµРЅС‚РѕРІ.

        Args:
            tool_name: РРјСЏ РёРЅСЃС‚СЂСѓРјРµРЅС‚Р°.
            args: РђСЂРіСѓРјРµРЅС‚С‹.

        Returns:
            ToolResult СЃ СЂРµР·СѓР»СЊС‚Р°С‚РѕРј РІС‹РїРѕР»РЅРµРЅРёСЏ.
        """
        if tool_name not in self.tool_map:
            return ToolResult(success=False, data="", error=f"РРЅСЃС‚СЂСѓРјРµРЅС‚ '{tool_name}' РЅРµ РЅР°Р№РґРµРЅ.")

        tool_fn = self.tool_map[tool_name]

        # Р’Р°Р»РёРґР°С†РёСЏ Р°СЂРіСѓРјРµРЅС‚РѕРІ: РїСЂРѕРІРµСЂСЏРµРј С‡С‚Рѕ РІСЃРµ РѕР±СЏР·Р°С‚РµР»СЊРЅС‹Рµ РїРѕР»СЏ РїСЂРёСЃСѓС‚СЃС‚РІСѓСЋС‚
        # Рё РЅРµС‚ Р»РёС€РЅРёС… РєР»СЋС‡РµР№ (LLM РјРѕР¶РµС‚ РіР°Р»Р»СЋС†РёРЅРёСЂРѕРІР°С‚СЊ РїР°СЂР°РјРµС‚СЂС‹)
        if hasattr(tool_fn, "args_schema") and tool_fn.args_schema is not None:
            schema = tool_fn.args_schema
            if hasattr(schema, "model_fields"):
                expected_fields = set(schema.model_fields.keys())
                required_fields = {
                    k for k, v in schema.model_fields.items()
                    if v.is_required()
                }
                provided_fields = set(args.keys())
                missing = required_fields - provided_fields
                if missing:
                    return ToolResult(
                        success=False, data="",
                        error=f"РћС€РёР±РєР° РёРЅСЃС‚СЂСѓРјРµРЅС‚Р° {tool_name}: "
                              f"РѕС‚СЃСѓС‚СЃС‚РІСѓСЋС‚ РѕР±СЏР·Р°С‚РµР»СЊРЅС‹Рµ РїР°СЂР°РјРµС‚СЂС‹: {', '.join(sorted(missing))}",
                    )
                extra = provided_fields - expected_fields
                if extra:
                    logger.warning(
                        "Tool %s: Р»РёС€РЅРёРµ Р°СЂРіСѓРјРµРЅС‚С‹ Р±СѓРґСѓС‚ РїСЂРѕРёРіРЅРѕСЂРёСЂРѕРІР°РЅС‹: %s",
                        tool_name, extra,
                    )
                    args = {k: v for k, v in args.items() if k in expected_fields}

        try:
            result = tool_fn.invoke(args)
            data = str(result)
            logger.info("Tool %s РІС‹РїРѕР»РЅРµРЅ СѓСЃРїРµС€РЅРѕ", tool_name)
            # РџСЂРѕРІРµСЂСЏРµРј РѕС€РёР±РєРё, РІРѕР·РІСЂР°С‰С‘РЅРЅС‹Рµ СЃР°РјРёРј РёРЅСЃС‚СЂСѓРјРµРЅС‚РѕРј РєР°Рє СЃС‚СЂРѕРєРё
            if data.startswith("РћС€РёР±РєР°"):
                return ToolResult(success=False, data=data, error=data)
            return ToolResult(success=True, data=data)
        except Exception as e:
            error_msg = f"РћС€РёР±РєР° РёРЅСЃС‚СЂСѓРјРµРЅС‚Р° {tool_name}: {e}"
            logger.error("Tool %s РѕС€РёР±РєР°: %s", tool_name, e)
            return ToolResult(success=False, data="", error=error_msg)

    @staticmethod
    def _parse_sql_tool_payload(raw: str) -> dict[str, Any] | None:
        """Parse structured SQL tool payload if present."""
        try:
            data = json.loads(raw)
        except (json.JSONDecodeError, TypeError):
            return None
        if not isinstance(data, dict):
            return None

        required = {"message", "preview_markdown", "total_rows", "is_empty", "saved_file", "mode"}
        if not required.issubset(set(data.keys())):
            return None

        try:
            return {
                "message": str(data.get("message", "")),
                "preview_markdown": str(data.get("preview_markdown", "")),
                "total_rows": int(data.get("total_rows", 0)),
                "is_empty": bool(data.get("is_empty", False)),
                "saved_file": data.get("saved_file"),
                "mode": str(data.get("mode", "")),
            }
        except (TypeError, ValueError):
            return None

    @staticmethod
    def _render_tool_result(raw: str, payload: dict[str, Any] | None = None) -> str:
        """Render tool output for logs/messages (human-readable)."""
        p = payload if payload is not None else GraphNodes._parse_sql_tool_payload(raw)
        if not p:
            return raw

        parts = [p.get("message", "")]
        preview = p.get("preview_markdown", "")
        if preview:
            parts.append(preview)
        saved_file = p.get("saved_file")
        if saved_file:
            parts.append(f"Файл: {saved_file}")
        return "\n".join(part for part in parts if part).strip()

    @staticmethod
    def _check_result_sanity(user_input: str, exec_result: str) -> list[str]:
        """Р­РІСЂРёСЃС‚РёС‡РµСЃРєРёРµ РїСЂРѕРІРµСЂРєРё РѕСЃРјС‹СЃР»РµРЅРЅРѕСЃС‚Рё СЂРµР·СѓР»СЊС‚Р°С‚Р° SQL-Р·Р°РїСЂРѕСЃР°.

        Р’РѕР·РІСЂР°С‰Р°РµС‚ СЃРїРёСЃРѕРє РїСЂРµРґСѓРїСЂРµР¶РґРµРЅРёР№ (РїСѓСЃС‚РѕР№ РµСЃР»Рё РІСЃС‘ РѕРє).
        """
        warnings = []
        user_lower = user_input.lower()

        # РЎС‡РёС‚Р°РµРј СЃС‚СЂРѕРєРё РІ markdown-С‚Р°Р±Р»РёС†Рµ (СЃС‚СЂРѕРєРё СЃ | РІ РЅР°С‡Р°Р»Рµ, РєСЂРѕРјРµ Р·Р°РіРѕР»РѕРІРєР° Рё СЂР°Р·РґРµР»РёС‚РµР»СЏ)
        result_lines = [
            line for line in exec_result.split("\n")
            if line.strip().startswith("|") and not line.strip().startswith("|---") and not line.strip().startswith("| ---")
        ]
        # РџРµСЂРІР°СЏ СЃС‚СЂРѕРєР° вЂ” Р·Р°РіРѕР»РѕРІРѕРє, РІС‚РѕСЂР°СЏ вЂ” СЂР°Р·РґРµР»РёС‚РµР»СЊ, РѕСЃС‚Р°Р»СЊРЅС‹Рµ вЂ” РґР°РЅРЅС‹Рµ
        data_row_count = max(0, len(result_lines) - 1)  # -1 РґР»СЏ Р·Р°РіРѕР»РѕРІРєР°

        # РџСЂРѕРІРµСЂРєР°: РІРѕРїСЂРѕСЃ В«СЃРєРѕР»СЊРєРѕ/РєРѕР»РёС‡РµСЃС‚РІРѕВ» РЅРѕ СЂРµР·СѓР»СЊС‚Р°С‚ вЂ” РЅРµСЃРєРѕР»СЊРєРѕ СЃС‚СЂРѕРє (РІРµСЂРѕСЏС‚РЅРѕ Р·Р°Р±С‹Р»Рё Р°РіСЂРµРіР°С†РёСЋ)
        count_patterns = re.compile(r'СЃРєРѕР»СЊРєРѕ|РєРѕР»РёС‡РµСЃС‚РІРѕ|РѕР±С‰РµРµ\s+С‡РёСЃР»Рѕ|РёС‚РѕРіРѕ|РІСЃРµРіРѕ', re.IGNORECASE)
        if count_patterns.search(user_lower) and data_row_count > 5:
            warnings.append(
                f"Р’РѕРїСЂРѕСЃ СЃРѕРґРµСЂР¶РёС‚ 'СЃРєРѕР»СЊРєРѕ/РєРѕР»РёС‡РµСЃС‚РІРѕ', РЅРѕ СЂРµР·СѓР»СЊС‚Р°С‚ СЃРѕРґРµСЂР¶РёС‚ {data_row_count} СЃС‚СЂРѕРє. "
                "Р’РѕР·РјРѕР¶РЅРѕ, РїСЂРѕРїСѓС‰РµРЅР° Р°РіСЂРµРіР°С†РёСЏ (COUNT, SUM) РёР»Рё GROUP BY."
            )

        # РџСЂРѕРІРµСЂРєР°: СЂРµР·СѓР»СЊС‚Р°С‚ СѓРїРёСЂР°РµС‚СЃСЏ РІ LIMIT РїСЂРё РЅР°Р»РёС‡РёРё JOIN
        if "РїРѕРєР°Р·Р°РЅРѕ" in exec_result and "РёР·" in exec_result:
            limit_match = re.search(r'РїРѕРєР°Р·Р°РЅРѕ\s+\d+\s+РёР·\s+(\d+)', exec_result)
            if limit_match and int(limit_match.group(1)) >= 1000:
                warnings.append(
                    f"Р РµР·СѓР»СЊС‚Р°С‚ СЃРѕРґРµСЂР¶РёС‚ {limit_match.group(1)}+ СЃС‚СЂРѕРє (СѓРїРёСЂР°РµС‚СЃСЏ РІ LIMIT). "
                    "Р•СЃР»Рё SQL СЃРѕРґРµСЂР¶РёС‚ JOIN вЂ” РІРѕР·РјРѕР¶РµРЅ row explosion."
                )

        # РџСЂРѕРІРµСЂРєР°: РЅРµ-СЃРїРёСЃРѕС‡РЅС‹Р№ РІРѕРїСЂРѕСЃ, РЅРѕ РѕС‡РµРЅСЊ РјРЅРѕРіРѕ СЃС‚СЂРѕРє
        listing_patterns = re.compile(
            r'СЃРїРёСЃРѕРє|РїРµСЂРµС‡РёСЃР»Рё|РІСЃРµ\s+(СЃС‚СЂРѕРєРё|Р·Р°РїРёСЃРё|РґР°РЅРЅС‹Рµ)|РїРѕРєР°Р¶Рё\s+РІСЃРµ', re.IGNORECASE,
        )
        if data_row_count > 100 and not listing_patterns.search(user_lower):
            warnings.append(
                f"Р РµР·СѓР»СЊС‚Р°С‚ СЃРѕРґРµСЂР¶РёС‚ {data_row_count} СЃС‚СЂРѕРє, С…РѕС‚СЏ РІРѕРїСЂРѕСЃ РЅРµ РїСЂРµРґРїРѕР»Р°РіР°РµС‚ СЃРїРёСЃРѕРє. "
                "РџСЂРѕРІРµСЂСЊ, РЅРµС‚ Р»Рё РґСѓР±Р»РёСЂРѕРІР°РЅРёСЏ РёР·-Р·Р° JOIN."
            )

        return warnings

    def _semantic_sql_check(self, user_input: str, sql: str) -> list[str]:
        """Проверить семантическое соответствие SQL запросу пользователя.

        Lightweight LLM-вызов для обнаружения типичных семантических ошибок:
        - Пропущенные фильтры дат (запрос за январь, но SQL без верхней границы)
        - COUNT(*) вместо COUNT(DISTINCT ...)
        - Неправильная гранулярность агрегации

        Args:
            user_input: Запрос пользователя.
            sql: Сгенерированный SQL.

        Returns:
            Список предупреждений (пустой если всё корректно).
        """
        system = (
            "Ты — валидатор SQL-запросов. Проверь, соответствует ли SQL запросу пользователя.\n"
            "Проверяй ТОЛЬКО:\n"
            "1. Фильтры дат: если запрос за конкретный период — есть ли ОБЕ границы (>= и <)?\n"
            "2. Агрегация: если спрашивают 'сколько уникальных' — используется ли COUNT(DISTINCT)?\n"
            "3. Гранулярность: если спрашивают итог — нет ли лишней детализации?\n"
            "4. GROUP BY: все ли не-агрегированные колонки перечислены?\n\n"
            "Верни JSON-массив строк с предупреждениями. Если всё корректно — верни [].\n"
            "Пример: [\"Пропущена верхняя граница даты: добавь AND date < '2024-02-01'\"]\n"
            "Верни ТОЛЬКО JSON-массив, без пояснений."
        )
        user = f"Запрос: {user_input}\n\nSQL:\n{sql}"

        try:
            response = self.llm.invoke_with_system(system, user, temperature=0.0)
            # Parse response
            cleaned = self._clean_llm_json(response)
            import re as _re
            match = _re.search(r'\[.*\]', cleaned, _re.DOTALL)
            if match:
                import json as _json
                warnings = _json.loads(match.group())
                if isinstance(warnings, list):
                    return [str(w) for w in warnings if w]
        except Exception as e:
            logger.warning("Semantic SQL check failed: %s", e)

        return []

    def sql_validator_node(self, state: AgentState) -> dict[str, Any]:
        """РЈР·РµР» РІР°Р»РёРґР°С†РёРё SQL: РїСЂРѕРІРµСЂСЏРµС‚ SQL РїРµСЂРµРґ РІС‹РїРѕР»РЅРµРЅРёРµРј.

        Args:
            state: РўРµРєСѓС‰РµРµ СЃРѕСЃС‚РѕСЏРЅРёРµ РіСЂР°С„Р°.

        Returns:
            РћР±РЅРѕРІР»РµРЅРёСЏ СЃРѕСЃС‚РѕСЏРЅРёСЏ.
        """
        sql = state.get("sql_to_validate")
        pending_call = state.get("pending_sql_tool_call")
        if not sql and pending_call:
            sql = pending_call.get("args", {}).get("sql")
        if not sql:
            return {"sql_to_validate": None, "pending_sql_tool_call": None}

        logger.info("Validator: РїСЂРѕРІРµСЂРєР° SQL: %s", sql[:200])
        result = self.validator.validate(sql)

        # РўСЂРµР±СѓРµС‚СЃСЏ РїРѕРґС‚РІРµСЂР¶РґРµРЅРёРµ РїРѕР»СЊР·РѕРІР°С‚РµР»СЏ вЂ” СЃРѕС…СЂР°РЅСЏРµРј SQL РІ СЃРѕСЃС‚РѕСЏРЅРёРё
        if result.needs_confirmation:
            return {
                "needs_confirmation": True,
                "confirmation_message": result.confirmation_message,
                "sql_to_validate": sql,
                "pending_sql_tool_call": pending_call,
                "messages": state["messages"] + [
                    {"role": "assistant", "content": f"вљ пёЏ {result.confirmation_message}"}
                ],
            }

        # РЎРѕС…СЂР°РЅСЏРµРј РёРЅС„РѕСЂРјР°С†РёСЋ Рѕ СЂРёСЃРєР°С… JOIN РґР»СЏ post-execution РїСЂРѕРІРµСЂРєРё
        join_risk_info = {}
        if result.join_checks:
            join_risk_info = {
                "multiplication_factor": result.multiplication_factor,
                "non_unique_joins": [
                    jc for jc in result.join_checks if not jc["is_unique"]
                ],
                "rewrite_suggestions": result.rewrite_suggestions,
            }

        # Р•СЃС‚СЊ РѕС€РёР±РєРё вЂ” РѕС‚РїСЂР°РІРёС‚СЊ РІ РєРѕСЂСЂРµРєС‚РѕСЂ
        if not result.is_valid:
            error_msg = result.summary()
            logger.warning("Validator: SQL РЅРµРІР°Р»РёРґРµРЅ: %s", error_msg[:200])
            return {
                "last_error": error_msg,
                "sql_to_validate": None,
                "pending_sql_tool_call": None,
                "join_risk_info": join_risk_info,
                "messages": state["messages"] + [
                    {"role": "assistant", "content": f"РћС€РёР±РєР° РІР°Р»РёРґР°С†РёРё:\n{error_msg}"}
                ],
            }

        # SQL РІР°Р»РёРґРµРЅ вЂ” РІС‹РїРѕР»РЅСЏРµРј
        tool_calls = state.get("tool_calls", [])
        last_tool = tool_calls[-1] if tool_calls else {}
        tool_name = (pending_call or {}).get("tool") or last_tool.get("tool", "execute_query")
        tool_args = dict((pending_call or {}).get("args", {}))
        if not tool_args:
            tool_args = {"sql": sql}

        t0 = time.monotonic()
        tool_result = self._call_tool(tool_name, tool_args)
        duration_ms = int((time.monotonic() - t0) * 1000)
        exec_result = str(tool_result)
        structured_payload = None
        if tool_name in self.SQL_TOOL_NAMES:
            structured_payload = self._parse_sql_tool_payload(exec_result)
        rendered_result = self._render_tool_result(exec_result, structured_payload)

        # РћС€РёР±РєР° РІС‹РїРѕР»РЅРµРЅРёСЏ вЂ” РѕС‚РїСЂР°РІР»СЏРµРј РІ РєРѕСЂСЂРµРєС‚РѕСЂ
        if not tool_result.success:
            self.memory.log_sql_execution(
                state["user_input"], sql, 0, "error", duration_ms,
                retry_count=state.get("retry_count", 0), error_type="execution",
            )
            return {
                "sql_to_validate": None,
                "pending_sql_tool_call": None,
                "last_error": tool_result.error,
                    "tool_calls": tool_calls[:-1] + [
                        {**last_tool, "result": rendered_result}
                    ],
                    "messages": state["messages"] + [
                        {"role": "assistant", "content": f"РћС€РёР±РєР° РІС‹РїРѕР»РЅРµРЅРёСЏ SQL:\n{rendered_result}"}
                    ],
                }

        # РџСЂРѕРІРµСЂРєР° РЅР° РїСѓСЃС‚РѕР№ СЂРµР·СѓР»СЊС‚Р°С‚ (0 СЃС‚СЂРѕРє) РґР»СЏ SELECT-Р·Р°РїСЂРѕСЃРѕРІ
        empty_result = False
        if tool_name == "execute_query" and structured_payload is not None:
            empty_result = bool(structured_payload.get("is_empty", False))
        elif tool_name == "execute_query" and (
            rendered_result == "Р—Р°РїСЂРѕСЃ РІС‹РїРѕР»РЅРµРЅ. Р РµР·СѓР»СЊС‚Р°С‚ РїСѓСЃС‚."
            or rendered_result.strip() == ""
        ):
            empty_result = True
            logger.warning("Validator: Р·Р°РїСЂРѕСЃ РІРµСЂРЅСѓР» 0 СЃС‚СЂРѕРє: %s", sql[:200])

        # РџСЂРµРґСѓРїСЂРµР¶РґРµРЅРёСЏ РёР· РІР°Р»РёРґР°С‚РѕСЂР°
        warnings_text = ""
        if result.warnings:
            warnings_text = "\nРџСЂРµРґСѓРїСЂРµР¶РґРµРЅРёСЏ:\n" + "\n".join(f"  вљ  {w}" for w in result.warnings)

        if empty_result:
            warnings_text += "\nвљ  Р—Р°РїСЂРѕСЃ РІРµСЂРЅСѓР» 0 СЃС‚СЂРѕРє. Р’РѕР·РјРѕР¶РЅРѕ, СѓСЃР»РѕРІРёСЏ С„РёР»СЊС‚СЂР°С†РёРё СЃР»РёС€РєРѕРј СЃС‚СЂРѕРіРёРµ " \
                             "РёР»Рё РґР°РЅРЅС‹Рµ РѕС‚СЃСѓС‚СЃС‚РІСѓСЋС‚. РџСЂРѕРІРµСЂСЊ СѓСЃР»РѕРІРёСЏ WHERE, Р·РЅР°С‡РµРЅРёСЏ С„РёР»СЊС‚СЂРѕРІ Рё С„РѕСЂРјР°С‚ РґР°С‚."


        # Семантическая проверка SQL (lightweight LLM call)
        semantic_warnings = self._semantic_sql_check(state["user_input"], sql)
        if semantic_warnings:
            semantic_text = "\n".join(f"\u26a0 Семантика: {w}" for w in semantic_warnings)
            warnings_text += "\n" + semantic_text
            logger.info("Semantic SQL warnings: %s", semantic_warnings)

        # РџРѕРґСЃС‡С‘С‚ СЃС‚СЂРѕРє РёР· markdown-С‚Р°Р±Р»РёС†С‹ (РЅСѓР¶РµРЅ РґР»СЏ sanity checks Рё post-exec detection)
        if structured_payload is not None:
            row_count = max(0, int(structured_payload.get("total_rows", 0)))
        else:
            data_lines = [l for l in rendered_result.split("\n") if l.strip().startswith("|")]
            row_count = max(0, len(data_lines) - 2) if data_lines else 0  # -2: Р·Р°РіРѕР»РѕРІРѕРє + СЂР°Р·РґРµР»РёС‚РµР»СЊ

        # Sanity checks СЂРµР·СѓР»СЊС‚Р°С‚Р° (Р±РёР·РЅРµСЃ-СѓСЂРѕРІРµРЅСЊ)
        if not empty_result and tool_name == "execute_query":
            sanity_source = (
                str(structured_payload.get("preview_markdown", ""))
                if structured_payload is not None
                else rendered_result
            )
            sanity_warnings = self._check_result_sanity(state["user_input"], sanity_source)
            for sw in sanity_warnings:
                warnings_text += f"\nвљ  {sw}"

        # Post-execution row explosion detection
        # РџРѕСЂРѕРі: factor > 1.5 (Р·РЅР°С‡РёРјРѕРµ СѓРјРЅРѕР¶РµРЅРёРµ) Р row_count > 50 (РЅРµ РїСЂРѕСЃС‚Рѕ С‚РµСЃС‚-Р·Р°РїСЂРѕСЃ).
        # factor=1.01 РїСЂРё 10 СЃС‚СЂРѕРєР°С… вЂ” С€СѓРј, РЅРµ explosion.
        if not empty_result and join_risk_info and tool_name == "execute_query":
            factor = join_risk_info.get("multiplication_factor", 1.0)
            if factor > 1.5 and row_count > 50:
                suggestions = "\n".join(join_risk_info.get("rewrite_suggestions", []))
                explosion_msg = (
                    f"POST-EXECUTION ROW EXPLOSION: Р РµР·СѓР»СЊС‚Р°С‚ СЃРѕРґРµСЂР¶РёС‚ {row_count} СЃС‚СЂРѕРє "
                    f"РїСЂРё multiplication_factor={factor:.1f}x. "
                    f"Р’РµСЂРѕСЏС‚РЅРѕ РґСѓР±Р»РёСЂРѕРІР°РЅРёРµ СЃС‚СЂРѕРє РёР·-Р·Р° JOIN.\n{suggestions}"
                )
                self.memory.log_sql_execution(
                    state["user_input"], sql, row_count, "row_explosion", duration_ms,
                    retry_count=state.get("retry_count", 0), error_type="join_explosion",
                )
                return {
                    "sql_to_validate": None,
                    "pending_sql_tool_call": None,
                    "last_error": explosion_msg,
                    "retry_count": state.get("retry_count", 0),
                    "join_risk_info": join_risk_info,
                    "tool_calls": tool_calls[:-1] + [
                        {**last_tool, "result": rendered_result}
                    ],
                    "messages": state["messages"] + [
                        {"role": "assistant", "content": f"вљ  {explosion_msg}"}
                    ],
                }

        # РђСѓРґРёС‚-Р»РѕРі SQL
        audit_status = "empty" if empty_result else "success"
        self.memory.log_sql_execution(
            state["user_input"], sql, row_count, audit_status, duration_ms,
            retry_count=state.get("retry_count", 0),
        )

        self.memory.add_message("tool", f"[{tool_name}] {rendered_result[:500]}")

        # Р•СЃР»Рё РїСѓСЃС‚РѕР№ СЂРµР·СѓР»СЊС‚Р°С‚ вЂ” РѕС‚РїСЂР°РІР»СЏРµРј РЅР° РєРѕСЂСЂРµРєС†РёСЋ, С‡С‚РѕР±С‹ LLM РїРµСЂРµСЃРјРѕС‚СЂРµР» Р·Р°РїСЂРѕСЃ
        if empty_result:
            return {
                "sql_to_validate": None,
                "pending_sql_tool_call": None,
                "last_error": (
                    f"SQL-Р·Р°РїСЂРѕСЃ РІС‹РїРѕР»РЅРµРЅ СѓСЃРїРµС€РЅРѕ, РЅРѕ РІРµСЂРЅСѓР» 0 СЃС‚СЂРѕРє. SQL: {sql}\n"
                    "РџСЂРѕРІРµСЂСЊ СѓСЃР»РѕРІРёСЏ WHERE, С„РѕСЂРјР°С‚ РґР°С‚, Р·РЅР°С‡РµРЅРёСЏ С„РёР»СЊС‚СЂРѕРІ. "
                    "РџРѕРїСЂРѕР±СѓР№ РѕСЃР»Р°Р±РёС‚СЊ СѓСЃР»РѕРІРёСЏ РёР»Рё РїСЂРѕРІРµСЂРёС‚СЊ РЅР°Р»РёС‡РёРµ РґР°РЅРЅС‹С… РІ С‚Р°Р±Р»РёС†Рµ."
                ),
                "retry_count": state.get("retry_count", 0),
                "join_risk_info": join_risk_info,
                "tool_calls": tool_calls[:-1] + [
                    {**last_tool, "result": rendered_result}
                ],
                "messages": state["messages"] + [
                    {"role": "assistant", "content": f"SQL РІС‹РїРѕР»РЅРµРЅ, РЅРѕ РІРµСЂРЅСѓР» 0 СЃС‚СЂРѕРє.{warnings_text}"}
                ],
            }

        return {
            "sql_to_validate": None,
            "pending_sql_tool_call": None,
            "last_error": None,
            "retry_count": 0,
            "join_risk_info": join_risk_info,
            "current_step": state["current_step"] + 1,
            "tool_calls": tool_calls[:-1] + [
                {**last_tool, "result": rendered_result}
            ],
            "messages": state["messages"] + [
                {"role": "assistant", "content": f"SQL РІС‹РїРѕР»РЅРµРЅ.{warnings_text}\n{rendered_result[:1000]}"}
            ],
        }

    def corrector(self, state: AgentState) -> dict[str, Any]:
        """РЈР·РµР» РєРѕСЂСЂРµРєС†РёРё: Р°РЅР°Р»РёР·РёСЂСѓРµС‚ РѕС€РёР±РєСѓ Рё РёСЃРїСЂР°РІР»СЏРµС‚ С€Р°Рі.

        Args:
            state: РўРµРєСѓС‰РµРµ СЃРѕСЃС‚РѕСЏРЅРёРµ РіСЂР°С„Р°.

        Returns:
            РћР±РЅРѕРІР»РµРЅРёСЏ СЃРѕСЃС‚РѕСЏРЅРёСЏ.
        """
        error = state.get("last_error", "")
        retry_count = state.get("retry_count", 0)
        step_idx = state["current_step"]
        plan = state["plan"]
        current_step = plan[step_idx] if step_idx < len(plan) else "РЅРµРёР·РІРµСЃС‚РЅС‹Р№ С€Р°Рі"
        iterations = state.get("graph_iterations", 0) + 1

        logger.info("Corrector: РїРѕРїС‹С‚РєР° %d/%d, РѕС€РёР±РєР°: %s", retry_count + 1, self.MAX_RETRIES, error[:200])

        if retry_count >= self.MAX_RETRIES:
            replan_count = state.get("replan_count", 0)
            # Попробовать replanning один раз перед сдачей
            if replan_count < 1:
                logger.info("Corrector: попытки исчерпаны, запрашиваю replanning")
                return {
                    "last_error": None,
                    "retry_count": 0,
                    "replan_count": replan_count + 1,
                    "needs_replan": True,
                    "replan_context": f"Шаг '{current_step}' не удался после {self.MAX_RETRIES} попыток. "
                                      f"Последняя ошибка: {error}",
                    "graph_iterations": iterations,
                }
            # Replanning уже был — сдаёмся
            return {
                "last_error": None,
                "current_step": step_idx + 1,
                "retry_count": 0,
                "graph_iterations": iterations,
                "final_answer": f"Не удалось выполнить шаг '{current_step}' после {self.MAX_RETRIES} попыток. "
                                f"Последняя ошибка: {error}",
            }

        recent_calls = state.get("tool_calls", [])[-3:]
        if recent_calls:
            *old_calls, last_call = recent_calls
            prev_context = "\n".join(
                f"  {tc['tool']}: {tc['result'][:500]}" for tc in old_calls
            )
            prev_context += f"\n  {last_call['tool']} (РїРѕР»РЅС‹Р№ СЂРµР·СѓР»СЊС‚Р°С‚):\n{last_call['result']}"
        else:
            prev_context = ""

        tables_context = state.get("tables_context", "")

        tables_detail = self._get_tables_detail_context(current_step + " " + prev_context)
        if tables_detail and tables_detail in tables_context:
            tables_detail = ""

        system_prompt = self._get_corrector_system_prompt()

        # Р­СЃРєР°Р»Р°С†РёСЏ СЃС‚СЂР°С‚РµРіРёРё РІ Р·Р°РІРёСЃРёРјРѕСЃС‚Рё РѕС‚ РЅРѕРјРµСЂР° РїРѕРїС‹С‚РєРё
        is_row_explosion = "ROW EXPLOSION" in error
        if retry_count == 0:
            strategy_hint = (
                "РЎС‚СЂР°С‚РµРіРёРё РёСЃРїСЂР°РІР»РµРЅРёСЏ:\n"
                "- 0 СЃС‚СЂРѕРє в†’ РІС‹Р·РѕРІРё get_sample, РїСЂРѕРІРµСЂСЊ С„РѕСЂРјР°С‚ РґР°РЅРЅС‹С…, РѕСЃР»Р°Р±СЊ WHERE\n"
                "- РћС€РёР±РєР° РєРѕР»РѕРЅРєРё в†’ РІС‹Р·РѕРІРё get_table_columns\n"
                "- РЎРёРЅС‚Р°РєСЃРёС‡РµСЃРєР°СЏ РѕС€РёР±РєР° в†’ РёСЃРїСЂР°РІСЊ SQL РїРѕ С‚РµРєСЃС‚Сѓ РѕС€РёР±РєРё\n"
                "- РќРµРѕР¶РёРґР°РЅРЅС‹Р№ COUNT в†’ РїСЂРѕРІРµСЂСЊ РіСЂР°РЅСѓР»СЏСЂРЅРѕСЃС‚СЊ (COUNT(DISTINCT ...) РІРјРµСЃС‚Рѕ COUNT(*))\n"
                "- ROW EXPLOSION в†’ РЎРќРђР§РђР›Рђ РІС‹Р·РѕРІРё get_sample РґР»СЏ С‚Р°Р±Р»РёС†С‹ СЃ РґСѓР±Р»СЏРјРё, "
                "РёР·СѓС‡Рё РїСЂРёС‡РёРЅСѓ РґСѓР±Р»РµР№ (СЃС‚Р°С‚СѓСЃС‹? С„Р°РєС‚С‹? С‚РµС…РЅРёС‡РµСЃРєРёРµ РґСѓР±Р»Рё?), "
                "Р·Р°С‚РµРј РІС‹Р±РµСЂРё СЃС‚СЂР°С‚РµРіРёСЋ: WHERE-С„РёР»СЊС‚СЂ / GROUP BY Р°РіСЂРµРіР°С†РёСЏ / DISTINCT. "
                "РќР• РїСЂРёРјРµРЅСЏР№ DISTINCT Р±РµР· РїРѕРЅРёРјР°РЅРёСЏ РїСЂРёС‡РёРЅС‹."
            )
        else:
            strategy_hint = (
                f"Р’РќРРњРђРќРР•: Р­С‚Рѕ РїРѕРїС‹С‚РєР° {retry_count + 1} РёР· {self.MAX_RETRIES}.\n"
                "РџСЂРµРґС‹РґСѓС‰РёР№ РїРѕРґС…РѕРґ РЅРµ СЃСЂР°Р±РѕС‚Р°Р» вЂ” РЎРњР•РќР РЎРўР РђРўР•Р“РР®:\n"
                "- РџРѕРїСЂРѕР±СѓР№ РґСЂСѓРіРѕР№ РёРЅСЃС‚СЂСѓРјРµРЅС‚ РґР»СЏ РґРёР°РіРЅРѕСЃС‚РёРєРё (get_sample, get_table_columns)\n"
                "- РџРµСЂРµС„РѕСЂРјСѓР»РёСЂСѓР№ SQL СЃ РЅСѓР»СЏ, РЅРµ РїР°С‚С‡Рё СЃС‚Р°СЂС‹Р№\n"
                "- РџСЂРѕРІРµСЂСЊ, РїСЂР°РІРёР»СЊРЅР°СЏ Р»Рё С‚Р°Р±Р»РёС†Р° РёСЃРїРѕР»СЊР·СѓРµС‚СЃСЏ"
            )
            if is_row_explosion:
                strategy_hint += (
                    "\n- ROW EXPLOSION РЅРµ РёСЃРїСЂР°РІР»РµРЅ! РћР±СЏР·Р°С‚РµР»СЊРЅС‹Р№ РїРѕСЂСЏРґРѕРє:\n"
                    "  1. Р’С‹Р·РѕРІРё get_sample РґР»СЏ С‚Р°Р±Р»РёС†С‹ СЃ РґСѓР±Р»СЏРјРё вЂ” СѓР·РЅР°Р№ РїСЂРёС‡РёРЅСѓ\n"
                    "  2. РЎС‚Р°С‚СѓСЃС‹/РІРµСЂСЃРёРё в†’ WHERE status='active' РёР»Рё DISTINCT ON (key)\n"
                    "  3. Р¤Р°РєС‚С‹ (С‚СЂР°РЅР·Р°РєС†РёРё, СЃРѕР±С‹С‚РёСЏ) в†’ GROUP BY + Р°РіСЂРµРіР°С†РёСЏ\n"
                    "  4. РўРµС…РЅРёС‡РµСЃРєРёРµ РґСѓР±Р»Рё в†’ DISTINCT РєР°Рє РїРѕСЃР»РµРґРЅРµРµ СЃСЂРµРґСЃС‚РІРѕ\n"
                    "  DISTINCT Р±РµР· РїРѕРЅРёРјР°РЅРёСЏ РїСЂРёС‡РёРЅС‹ вЂ” Р—РђРџР Р•Р©РЃРќ"
                )

        # РР·РІР»РµРєР°РµРј РїРѕР»РЅС‹Р№ SQL РёР· РїРѕСЃР»РµРґРЅРµРіРѕ tool_call (РµСЃР»Рё РµСЃС‚СЊ)
        last_sql = ""
        if recent_calls:
            last_args = recent_calls[-1].get("args", {})
            if isinstance(last_args, dict):
                last_sql = last_args.get("sql", "")

        user_parts = []
        user_parts.append(f"[Р—РђРџР РћРЎ РџРћР›Р¬Р—РћР’РђРўР•Р›РЇ]\n{state['user_input']}")
        user_parts.append(f"[РџРћРџР«РўРљРђ {retry_count + 1} РёР· {self.MAX_RETRIES}]")
        user_parts.append(f"[РЁРђР“]\n{current_step}")
        if last_sql:
            user_parts.append(f"[SQL РљРћРўРћР Р«Р™ Р’Р«Р—Р’РђР› РћРЁРР‘РљРЈ]\n{last_sql}")
        user_parts.append(f"[РћРЁРР‘РљРђ]\n{error}")
        user_parts.append(strategy_hint)
        # РџРµСЂРµРґР°С‘Рј join_risk_info РґР»СЏ row explosion РѕС€РёР±РѕРє
        risk = state.get("join_risk_info", {})
        if risk and is_row_explosion:
            non_unique = risk.get("non_unique_joins", [])
            risk_details = "\n".join(
                f"  {jc.get('table', '?')}.{jc.get('columns', '?')} вЂ” РґСѓР±Р»Рё: {jc.get('duplicate_pct', '?')}%"
                for jc in non_unique
            )
            user_parts.append(
                f"[JOIN RISK INFO]\n"
                f"Multiplication factor: {risk.get('multiplication_factor', '?')}x\n"
                f"РџСЂРѕР±Р»РµРјРЅС‹Рµ РєР»СЋС‡Рё:\n{risk_details}"
            )
        if tables_context:
            user_parts.append(f"[РљРћРќРўР•РљРЎРў РўРђР‘Р›РР¦]\n{tables_context}")
        if tables_detail:
            user_parts.append(tables_detail)
        if prev_context:
            user_parts.append(f"[РџР Р•Р”Р«Р”РЈР©РР• Р’Р«Р—РћР’Р«]\n{prev_context}")

        user_prompt = "\n\n".join(user_parts)

        system_prompt, user_prompt = self._trim_to_budget(system_prompt, user_prompt)

        if self.debug_prompt:
            print(f"\n{'='*80}\n[DEBUG PROMPT вЂ” corrector]\n{'='*80}\n"
                  f"SYSTEM:\n{system_prompt}\n\nUSER:\n{user_prompt}\n{'='*80}\n")

        response = self.llm.invoke_with_system(system_prompt, user_prompt, temperature=0.2)
        tool_call = self._parse_tool_call(response)

        # Р•СЃР»Рё РёСЃРїСЂР°РІР»РµРЅРЅС‹Р№ РІС‹Р·РѕРІ СЃРѕРґРµСЂР¶РёС‚ SQL вЂ” РѕС‚РїСЂР°РІРёС‚СЊ РЅР° РІР°Р»РёРґР°С†РёСЋ
        sql = tool_call.get("args", {}).get("sql")
        if sql and tool_call["tool"] in self.SQL_TOOL_NAMES:
            # РЎРѕС…СЂР°РЅСЏРµРј РїСЂРёРјРµСЂ РёСЃРїСЂР°РІР»РµРЅРёСЏ РґР»СЏ РѕР±СѓС‡РµРЅРёСЏ
            example = f"РћС€РёР±РєР°: {error[:100]} в†’ РСЃРїСЂР°РІР»РµРЅРёРµ: {sql[:150]}"
            correction_examples = state.get("correction_examples", []) + [example]
            return {
                "sql_to_validate": sql,
                "pending_sql_tool_call": {
                    "tool": tool_call["tool"],
                    "args": dict(tool_call.get("args", {})),
                    "step_idx": step_idx,
                },
                "retry_count": retry_count + 1,
                "last_error": None,
                "graph_iterations": iterations,
                "correction_examples": correction_examples,
                "tool_calls": state.get("tool_calls", []) + [
                    {"tool": tool_call["tool"], "args": tool_call.get("args", {}), "result": "awaiting_validation"}
                ],
            }

        # Р’С‹Р·РѕРІ РёСЃРїСЂР°РІР»РµРЅРЅРѕРіРѕ РёРЅСЃС‚СЂСѓРјРµРЅС‚Р°
        tool_result = self._call_tool(tool_call["tool"], tool_call.get("args", {}))
        result_str = str(tool_result)

        if not tool_result.success:
            return {
                "last_error": tool_result.error,
                "retry_count": retry_count + 1,
                "graph_iterations": iterations,
                "messages": state["messages"] + [
                    {"role": "assistant", "content": f"РџРѕРІС‚РѕСЂРЅР°СЏ РѕС€РёР±РєР° (РїРѕРїС‹С‚РєР° {retry_count + 1}): {result_str}"}
                ],
            }

        self.memory.add_message("tool", f"[corrector:{tool_call['tool']}] {result_str[:500]}")

        return {
            "tool_calls": state.get("tool_calls", []) + [
                {"tool": tool_call["tool"], "args": tool_call.get("args", {}), "result": result_str}
            ],
            "current_step": step_idx + 1,
            "last_error": None,
            "retry_count": 0,
            "sql_to_validate": None,
            "pending_sql_tool_call": None,
            "graph_iterations": iterations,
            "messages": state["messages"] + [
                {"role": "assistant", "content": f"РСЃРїСЂР°РІР»РµРЅРѕ. РЁР°Рі {step_idx + 1}: {result_str[:1000]}"}
            ],
        }

    def summarizer(self, state: AgentState) -> dict[str, Any]:
        """РЈР·РµР» С„РѕСЂРјРёСЂРѕРІР°РЅРёСЏ С„РёРЅР°Р»СЊРЅРѕРіРѕ РѕС‚РІРµС‚Р° РїРѕР»СЊР·РѕРІР°С‚РµР»СЋ.

        Args:
            state: РўРµРєСѓС‰РµРµ СЃРѕСЃС‚РѕСЏРЅРёРµ РіСЂР°С„Р°.

        Returns:
            РћР±РЅРѕРІР»РµРЅРёСЏ СЃРѕСЃС‚РѕСЏРЅРёСЏ СЃ final_answer.
        """
        # РЎРѕС…СЂР°РЅСЏРµРј РїСЂРёРјРµСЂС‹ РёСЃРїСЂР°РІР»РµРЅРёР№ РІ РґРѕР»РіРѕСЃСЂРѕС‡РЅСѓСЋ РїР°РјСЏС‚СЊ
        new_examples = state.get("correction_examples", [])
        if new_examples:
            existing = self.memory.get_memory_list("correction_examples")
            combined = (existing + new_examples)[-20:]  # С…СЂР°РЅРёРј РїРѕСЃР»РµРґРЅРёРµ 20
            self.memory.set_memory("correction_examples", json.dumps(combined, ensure_ascii=False))

        # Р•СЃР»Рё СѓР¶Рµ РµСЃС‚СЊ С„РёРЅР°Р»СЊРЅС‹Р№ РѕС‚РІРµС‚ (РЅР°РїСЂРёРјРµСЂ, РѕС‚ corrector РїСЂРё РёСЃС‡РµСЂРїР°РЅРёРё РїРѕРїС‹С‚РѕРє)
        if state.get("final_answer"):
            self.memory.add_message("assistant", state["final_answer"])
            return {}

        tool_results_parts = []
        for tc in state.get("tool_calls", []):
            sql = tc.get("args", {}).get("sql", "")
            sql_line = f"\n  SQL: {sql}" if sql else ""
            tool_results_parts.append(
                f"- {tc['tool']}{sql_line}\n  Р РµР·СѓР»СЊС‚Р°С‚: {tc['result'][:5000]}"
            )
        tool_results = "\n".join(tool_results_parts)

        plan_text = "\n".join(state.get("plan", []))

        system_prompt = self._get_summarizer_system_prompt()

        # РљСЂР°С‚РєРёР№ РєРѕРЅС‚РµРєСЃС‚ С‚Р°Р±Р»РёС† (РѕРїРёСЃР°РЅРёСЏ РєРѕР»РѕРЅРѕРє Р±РµР· СЃРµРјРїР»РѕРІ)
        tables_ctx = state.get("tables_context", "")
        tables_summary = ""
        if tables_ctx:
            # РР·РІР»РµРєР°РµРј С‚РѕР»СЊРєРѕ СЃС‚СЂРѕРєРё СЃ РѕРїРёСЃР°РЅРёСЏРјРё РєРѕР»РѕРЅРѕРє (Р±РµР· СЃРµРјРїР»РѕРІ РґР°РЅРЅС‹С…)
            ctx_lines = tables_ctx.split("\n")
            summary_lines = []
            skip_sample = False
            for line in ctx_lines:
                if "РћР±СЂР°Р·РµС† РґР°РЅРЅС‹С…" in line or "Sample" in line:
                    skip_sample = True
                    continue
                if skip_sample and (line.strip() == "" or line.startswith("[")):
                    skip_sample = False
                if not skip_sample:
                    summary_lines.append(line)
            tables_summary = "\n".join(summary_lines).strip()

        user_parts_sum = [f"Р—Р°РїСЂРѕСЃ РїРѕР»СЊР·РѕРІР°С‚РµР»СЏ: {state['user_input']}"]
        user_parts_sum.append(f"РџР»Р°РЅ:\n{plan_text}")
        if tables_summary:
            user_parts_sum.append(f"РљРѕРЅС‚РµРєСЃС‚ С‚Р°Р±Р»РёС† (РґР»СЏ РёРЅС‚РµСЂРїСЂРµС‚Р°С†РёРё РєРѕР»РѕРЅРѕРє):\n{tables_summary}")
        user_parts_sum.append(f"Р РµР·СѓР»СЊС‚Р°С‚С‹ РёРЅСЃС‚СЂСѓРјРµРЅС‚РѕРІ:\n{tool_results}")
        user_prompt = "\n\n".join(user_parts_sum)

        system_prompt, user_prompt = self._trim_to_budget(system_prompt, user_prompt)

        if self.debug_prompt:
            print(f"\n{'='*80}\n[DEBUG PROMPT вЂ” summarizer]\n{'='*80}\n"
                  f"SYSTEM:\n{system_prompt}\n\nUSER:\n{user_prompt}\n{'='*80}\n")

        answer = self.llm.invoke_with_system(system_prompt, user_prompt, temperature=0.2)
        self.memory.add_message("assistant", answer)

        logger.info("Summarizer: РѕС‚РІРµС‚ СЃС„РѕСЂРјРёСЂРѕРІР°РЅ")
        return {
            "final_answer": answer,
            "messages": state["messages"] + [
                {"role": "assistant", "content": answer}
            ],
        }



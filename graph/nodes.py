    def _get_schema_context(self, user_input: str = "") -> str:
        """РЎС„РѕСЂРјРёСЂРѕРІР°С‚СЊ РєСЂР°С‚РєРёР№ РєР°С‚Р°Р»РѕРі С‚Р°Р±Р»РёС† РёР· SchemaLoader.

        РџСЂРё Р±РѕР»СЊС€РѕРј РєР°С‚Р°Р»РѕРіРµ (>50 С‚Р°Р±Р»РёС†) РІС‹РїРѕР»РЅСЏРµС‚ РїСЂРµРґС„РёР»СЊС‚СЂР°С†РёСЋ РїРѕ РєР»СЋС‡РµРІС‹Рј
        СЃР»РѕРІР°Рј РёР· Р·Р°РїСЂРѕСЃР° РїРѕР»СЊР·РѕРІР°С‚РµР»СЏ, С‡С‚РѕР±С‹ РЅРµ РїРµСЂРµРіСЂСѓР¶Р°С‚СЊ РєРѕРЅС‚РµРєСЃС‚ LLM.
        """
        df = self.schema.tables_df
        if df.empty:
            return "РљР°С‚Р°Р»РѕРі С‚Р°Р±Р»РёС† РїСѓСЃС‚. РСЃРїРѕР»СЊР·СѓР№ search_tables РґР»СЏ РїРѕРёСЃРєР°."

